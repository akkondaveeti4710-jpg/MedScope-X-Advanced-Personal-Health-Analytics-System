def generate_report(name,bmi,bmr,hydration,cardio,diabetes,health):

    print("\n====== MEDSCOPE X REPORT ======\n")

    print("Patient:",name)

    print("BMI:",bmi)

    print("BMR:",bmr)

    print("Hydration Score:",hydration)

    print("Cardiovascular Risk:",cardio)

    print("Diabetes Risk:",diabetes)

    print("\nOverall Health Score:",health,"/100")
